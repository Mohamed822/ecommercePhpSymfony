/**
 * @method User|null find($id, $lockMode = null, $lockVersion = null)
 * @method User|null findOneBy(array $criteria, array $orderBy = null)
 * @method User[]    findAll()
 * @method User[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class UserRepository extends ServiceEntityRepository
{
    private $manager;

    public function __construct(ManagerRegistry $registry,
    EntityManagerInterface $manager
    )
    {
        parent::__construct($registry, User::class);
        $this->manager = $manager;

    }

    /**
     * @throws ORMException
     * @throws OptimisticLockException
     */

    public function saveuser($firstName, $lastName, $email, $login,$password)
    {
            $newuser = new user();
            $hashPass = password_hash($password,PASSWORD_DEFAULT);
            $newuser
                ->setFirstName($firstName)
                ->setLastName($lastName)
                ->setEmail($email)
                ->setPassword($hashPass)
                ->setLogin($login);
            $this->manager->persist($newuser);
            $this->manager->flush();    
    }

    public function updateuser(user $user, $data)
    {
        empty($data['firstName']) ? true : $user->setFirstName($data['firstName']);
        empty($data['lastName']) ? true : $user->setLastName($data['lastName']);
        empty($data['email']) ? true : $user->setEmail($data['email']);
        empty($data['login']) ? true : $user->setLogin($data['login']);

        $this->manager->flush();
    }

    public function removeuser(user $user)
    {
        $this->manager->remove($user);
        $this->manager->flush();
    } 
    public function add(User $entity, bool $flush = true): void
    {
        $this->_em->persist($entity);
        if ($flush) {
            $this->_em->flush();
        }
    }

    /**
     * @throws ORMException
     * @throws OptimisticLockException
     */
    public function remove(User $entity, bool $flush = true): void
    {
        $this->_em->remove($entity);
        if ($flush) {
            $this->_em->flush();
        }
    }

